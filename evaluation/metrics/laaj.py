"""
evaluation/metrics/laaj.py

LaaJ — LLM-as-a-Judge.

BLEU and ROUGE compare strings. RAGAS decomposes claims. Neither of them can
say "this answer is correct but incomplete, and it misstates the cure period
as 10 days when the contract says 30". That judgement is what LaaJ adds:
a model reads the question, the retrieved context, the generated answer and
the ground truth, then scores four dimensions on a 1-5 rubric with written
reasoning.

Dimensions scored:

    correctness    Does it match the ground truth on the facts?
                   The dimension that matters most for contract Q&A —
                   a wrong dollar figure is a wrong answer.

    completeness   Does it cover everything the ground truth covers?
                   Catches partial answers that state one of three
                   termination grounds and stop.

    groundedness   Is every statement traceable to the provided context?
                   Overlaps with RAGAS faithfulness deliberately — the two
                   disagreeing is a useful signal that one judge run drifted.

    clarity        Is it plain English a non-lawyer can act on?
                   This is what the product actually promises, so it is
                   worth scoring explicitly rather than assuming.

Known limitations, stated plainly because they affect how much these numbers
are worth:
  - The judge (llama3.2) is the same model family as the generator, so it
    shows self-preference bias and rates its own phrasing generously.
  - Small local models compress toward the middle of a 1-5 scale and rarely
    award 1 or 5. Watch the relative ordering across runs, not the absolute.
  - Temperature is 0, so runs are reproducible, but reproducible is not
    the same as accurate.
A stronger judge model would tighten all four numbers. Swap JUDGE_MODEL in
evaluation/metrics/_llm.py to test that.
"""

from evaluation.metrics._llm import judge_json


DIMENSIONS = ("correctness", "completeness", "groundedness", "clarity")

MAX_SCORE = 5


JUDGE_PROMPT = """You are an impartial evaluator scoring the output of a legal-contract question answering system.

You will be given a QUESTION, the CONTEXT the system retrieved, the ANSWER it generated, and the REFERENCE ANSWER written by a human who read the full contract.

Score the ANSWER on four dimensions, each from 1 to 5.

correctness — Do the facts in the answer match the reference answer?
  5 = every fact matches the reference
  4 = matches with a trivial imprecision that changes nothing
  3 = mostly right but one meaningful fact is wrong or vague
  2 = several facts wrong, or the central fact is wrong
  1 = contradicts the reference, or invents facts

completeness — Does the answer cover everything the reference covers?
  5 = covers every point in the reference
  4 = covers the main points, omits a minor detail
  3 = covers roughly half the reference
  2 = covers one point out of several
  1 = answers almost nothing the reference answers

groundedness — Is every statement supported by the CONTEXT provided?
  5 = every statement traces to the context
  4 = all key statements grounded, minor unsupported phrasing
  3 = mostly grounded but contains an unsupported claim
  2 = substantially relies on information not in the context
  1 = largely fabricated relative to the context

clarity — Is it plain English that a non-lawyer could act on?
  5 = clear, direct, no unexplained jargon
  4 = clear with minor legalese
  3 = understandable but wordy or hedge-heavy
  2 = hard to follow, dense legalese
  1 = incomprehensible to a non-lawyer

Be strict. Do not give a 5 unless the answer earns it. If the answer says it
could not find the information but the reference contains it, correctness and
completeness are 1.

QUESTION:
{question}

CONTEXT PROVIDED TO THE SYSTEM:
{context}

ANSWER GENERATED BY THE SYSTEM:
{answer}

REFERENCE ANSWER:
{ground_truth}

Reply with ONLY this JSON object and nothing else:
{{
  "correctness": {{"score": 3, "reason": "..."}},
  "completeness": {{"score": 3, "reason": "..."}},
  "groundedness": {{"score": 3, "reason": "..."}},
  "clarity": {{"score": 3, "reason": "..."}},
  "overall_verdict": "one sentence summarising the answer's quality"
}}
"""


def _clamp_score(value) -> int | None:
    """Judges occasionally return '4/5', 4.0, or an out-of-range number."""
    if isinstance(value, bool):
        return None
    if isinstance(value, (int, float)):
        score = int(round(value))
    elif isinstance(value, str):
        digits = "".join(ch for ch in value.split("/")[0] if ch.isdigit())
        if not digits:
            return None
        score = int(digits)
    else:
        return None
    return max(1, min(MAX_SCORE, score))


def judge_answer(question: str, answer: str, context: str, ground_truth: str) -> dict:
    """
    Scores one answer across the four rubric dimensions.

    Returns per-dimension scores with the judge's reasoning, plus a normalised
    0-1 mean so LaaJ sits on the same scale as BLEU / ROUGE / RAGAS in the
    summary table.
    """
    if not answer.strip():
        return {"scores": {}, "error": "empty answer"}

    verdict = judge_json(
        JUDGE_PROMPT.format(
            question=question,
            context=context[:8000] if context else "(no context retrieved)",
            answer=answer,
            ground_truth=ground_truth,
        )
    )

    if not isinstance(verdict, dict):
        return {"scores": {}, "error": "judge did not return parseable JSON"}

    scores = {}
    reasons = {}
    for dimension in DIMENSIONS:
        entry = verdict.get(dimension)
        if isinstance(entry, dict):
            score = _clamp_score(entry.get("score"))
            reasons[dimension] = str(entry.get("reason", ""))[:400]
        else:
            # Some runs return a bare number instead of {"score": n, "reason": ...}
            score = _clamp_score(entry)
            reasons[dimension] = ""
        if score is not None:
            scores[dimension] = score

    if not scores:
        return {"scores": {}, "error": "no dimension scores parsed"}

    mean_raw = sum(scores.values()) / len(scores)

    return {
        "scores": scores,
        "reasons": reasons,
        "overall_verdict": str(verdict.get("overall_verdict", ""))[:400],
        "mean_score_5": round(mean_raw, 2),
        # Map 1-5 onto 0-1 so it is comparable with the other three metrics.
        "normalised_score": round((mean_raw - 1) / (MAX_SCORE - 1), 4),
    }


def aggregate_laaj(all_results: list[dict]) -> dict:
    """Averages each rubric dimension across the test set."""
    valid = [r for r in all_results if r.get("scores")]
    if not valid:
        return {"error": "no valid judge results"}

    aggregated = {}
    for dimension in DIMENSIONS:
        values = [r["scores"][dimension] for r in valid if dimension in r["scores"]]
        if values:
            aggregated[dimension] = round(sum(values) / len(values), 2)

    normalised = [r["normalised_score"] for r in valid if r.get("normalised_score") is not None]

    return {
        "per_dimension_5": aggregated,
        "mean_score_5": round(sum(aggregated.values()) / len(aggregated), 2) if aggregated else None,
        "normalised_score": round(sum(normalised) / len(normalised), 4) if normalised else None,
        "judged": len(valid),
        "total": len(all_results),
    }
